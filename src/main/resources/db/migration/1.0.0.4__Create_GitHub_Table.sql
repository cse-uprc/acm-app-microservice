CREATE TABLE github (
	git_id      INT          UNSIGNED NOT NULL AUTO_INCREMENT,
    repo_url    VARCHAR(256) NOT NULL,
    insert_date DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(git_id)
)ENGINE=InnoDB;

